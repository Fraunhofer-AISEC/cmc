func Test_verifyAzureMeasurements(t *testing.T) {
	type args struct {
		measurements []ar.Measurement
		nonce        []byte
		rootManifest *ar.MetadataResult
		tdxRefVals   []ar.ReferenceValue
		snpRefVals   []ar.ReferenceValue
		vtpmRefVals  []ar.ReferenceValue
		s            ar.Serializer
	}
	tests := []struct {
		name  string
		args  args
		want  []ar.MeasurementResult
		want1 bool
	}{
		// TODO does currently not work, as TCB is outdated
		// {
		// 	name: "Successful Azure Verification",
		// 	args: args{
		// 		measurements: validTdxMeasurements,
		// 		rootManifest: validTdxRootManifest,
		// 		tdxRefVals:   validTdxRefvalsAzure,
		// 		vtpmRefVals:  validVtpmRefvalsAzure,
		// 		nonce:        validVtpmNonce,
		// 	},
		// 	want:  successfulAzureResult,
		// 	want1: true,
		// },
		{
			name: "Failed Azure Verification Invalid Fingerprint",
			args: args{
				measurements: validTdxMeasurements,
				rootManifest: invalidTdxRootManifestFingerprint, // Invalid fingerprint
				tdxRefVals:   validTdxRefvalsAzure,
				vtpmRefVals:  validVtpmRefvalsAzure,
				nonce:        validVtpmNonce,
			},
			want:  failedAzureResultTdx,
			want1: false,
		},
		{
			name: "Failed Azure Verification Invalid TDX Refvals",
			args: args{
				measurements: validTdxMeasurements,
				rootManifest: validTdxRootManifest,
				tdxRefVals:   invalidTdxRefvalsAzure, // Invalid TDX refvals
				vtpmRefVals:  validVtpmRefvalsAzure,
				nonce:        validVtpmNonce,
			},
			want:  failedAzureResultTdx,
			want1: false,
		},
		{
			name: "Failed Azure Verification Invalid vTPM Refvals",
			args: args{
				measurements: validTdxMeasurements,
				rootManifest: validTdxRootManifest,
				tdxRefVals:   validTdxRefvalsAzure,
				vtpmRefVals:  invalidVtpmRefvalsAzure, // Invalid vTPM refvals azure
				nonce:        validVtpmNonce,
			},
			want:  failedAzureResultVtpm,
			want1: false,
		},
	}

	logrus.SetLevel(logrus.TraceLevel)

	collateral, err := FetchCollateral(fmspc_azure, pck_cert_azure, TDX_QUOTE_TYPE)
	if err != nil {
		t.Errorf("failed to get TDX collateral: %v", err)
	}

	for _, tt := range tests {

		// Insert collateral
		for i := range tt.args.measurements {
			if tt.args.measurements[i].Type == "Azure TDX Measurement" {

				tt.args.measurements[i].Artifacts = []ar.Artifact{
					{
						Type: "TDX Collateral",
						Events: []ar.MeasureEvent{
							{
								IntelCollateral: collateral,
							},
						},
					},
				}
			}
		}

		t.Run(tt.name, func(t *testing.T) {
			got, got1 := verifyAzureMeasurements(tt.args.measurements, tt.args.nonce, tt.args.rootManifest, tt.args.tdxRefVals, tt.args.snpRefVals, tt.args.vtpmRefVals, tt.args.s)
			for i := range got {
				if got[i].Summary.Success != tt.want[i].Summary.Success {
					t.Errorf("got %v: %v, want %v: %v", got[i].Type, got[i].Summary.Success, tt.want[i].Type, tt.want[i].Summary.Success)
				}
			}
			if got1 != tt.want1 {
				t.Errorf("verifyAzureMeasurements() got1 = %v, want %v", got1, tt.want1)
			}
		})
	}
}

