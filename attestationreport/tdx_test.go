func fetchLatestTcbInfo(tdx bool, fmspc string) ([]byte, error) {
	var uri string
	if tdx {
		uri = fmt.Sprintf("https://api.trustedservices.intel.com/tdx/certification/v4/tcb?fmspc=%v", fmspc)
	} else {
		uri = fmt.Sprintf("https://api.trustedservices.intel.com/sgx/certification/v4/tcb?fmspc=%v", fmspc)
	}
	resp, err := http.Get(uri)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("request failed with status code: %d", resp.StatusCode)
	}

	tcbInfoRaw, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	return tcbInfoRaw, nil
}

func fetchLatestQeIdentity(tdx bool) ([]byte, error) {
	var uri string
	if tdx {
		uri = `https://api.trustedservices.intel.com/tdx/certification/v4/qe/identity`
	} else {
		uri = `https://api.trustedservices.intel.com/sgx/certification/v4/qe/identity`
	}
	resp, err := http.Get(uri)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("request failed with status code: %d", resp.StatusCode)
	}

	qeIdentityRaw, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	return qeIdentityRaw, nil
}

