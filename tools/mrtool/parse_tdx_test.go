package main

import "testing"

func Test_parseEventlog(t *testing.T) {
	type args struct {
		data         []byte
		eventlogFlag bool
		summaryFlag  bool
		mrs          []int
	}
	tests := []struct {
		name    string
		args    args
		wantErr bool
	}{
		{
			name: "Parse CC Eventlog",
			args: args{
				data:         CC_EVENTLOG,
				eventlogFlag: true,
				summaryFlag:  true,
				mrs:          []int{1, 2, 3, 4},
			},
			wantErr: false,
		},
		{
			name: "Parse Invalid Eventlog",
			args: args{
				data:         []byte{0x01, 0x00, 0x00, 0x00, 0xff, 0xff, 0xff, 0xff},
				eventlogFlag: true,
				summaryFlag:  true,
				mrs:          []int{1, 2, 3, 4},
			},
			wantErr: true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if err := parseEventlog(tt.args.data, tt.args.eventlogFlag, tt.args.summaryFlag, tt.args.mrs); (err != nil) != tt.wantErr {
				t.Errorf("parseEventlog() error = %v, wantErr %v", err, tt.wantErr)
			}
		})
	}
}

